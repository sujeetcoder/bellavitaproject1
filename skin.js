function display(elem){
    

    let div = document.createElement("div");
    
    
    let img = document.createElement("img");
    img.setAttribute("src",elem.imageUrl);

    let name = document.createElement("h5");
    name.innerText = elem.name

    let description = document.createElement("p");
    description.innerText = elem.description

    let prdiv = document.createElement("div");

    let price = document.createElement("h4");

    let rupee = document.createElement("h4");
    rupee.innerText = "₹"
    
    price.innerText = elem.price;

    let stackedprice = document.createElement("p");
    stackedprice.innerText = elem.stackedPrice;
    

    let rating= document.createElement("h3");
    rating.innerText = elem.rating;


    let cartButton = document.createElement("button");
    cartButton.innerText = "ADD TO CART";

    document.querySelector("#container").append(div)
    div.append(img,name,description,prdiv,cartButton)
    prdiv.append(rupee,price,stackedprice,rating)

    cartButton.addEventListener("click",function(){
        if(check()){
            alert("ALREADY ADDED TO CART")
        }else{
            cart.push(elem)
        localStorage.setItem("cartData",JSON.stringify(cart))
        window.location.href="cart.html"
        }
        
    })

    function check(){
        for(let i=0; i<cart.length; i++){
            if(cart[i].productID == elem.productID){
                return true;
            }
        }
        return false;
    }
    div.addEventListener("mouseover",mouseOn)
    function mouseOn(){
        div.style.cursor = "pointer"
        img.setAttribute("src",elem.hoverUrl)
    }

    div.addEventListener("mouseout",mouseoff)
    function mouseoff(){
        img.setAttribute("src",elem.imageUrl)
    }

    img.addEventListener("click",redirect)
    name.addEventListener("click",redirect)

    function redirect(){
        localStorage.setItem("desciption",JSON.stringify(elem))
        window.location.href = "skin-description/s-12.html"
    }


    // /////////////////////////////////////////

    document.querySelector("#featured").addEventListener("change", function(){
        filterit(document.querySelector("#featured").value,skinData)
    })
    /* filtred run program */
    function filterit(getvalue,skinData){
        if(getvalue=="Alpha"){
            filterbyAZ(skinData)
        }else if(getvalue=="rev Alpha"){
            filterbyZA(skinData)
        }else if(getvalue=="LH"){
            filterbyLH(skinData)
        }else if(getvalue=="HL"){
            filterbyHL(skinData)
        }
    }
    /* Sorting Functions */
    /* Alphabatic */
    function filterbyAZ(skinData){
        
        let filter = skinData.sort(function(a,b){
            if(a.name>b.name) return 1
            if(a.name<b.name) return -1
            return 0
        })
        document.querySelector("#container").innerHTML = ""
        filter.forEach(display)
    }
    function filterbyZA(skinData){
        let filter = skinData.sort(function(a,b){
            if(a.name>b.name) return -1
            if(a.name<b.name) return 1
            return 0
        })
        document.querySelector("#container").innerHTML = ""
        filter.forEach(display)
    }
    /* Price based */
    function filterbyLH(skinData){
        let filter = skinData.sort(function(a,b){
            return a.price - b.price
        })
        document.querySelector("#container").innerHTML = ""
        filter.forEach(display)
    }
    function filterbyHL(skinData){
        let filter = skinData.sort(function(a,b){
            return b.price - a.price
        })
        document.querySelector("#container").innerHTML = ""
        filter.forEach(display)
    }
}






